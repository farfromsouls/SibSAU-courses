import math
from scipy.optimize import minimize

# зададим исходные данные в километрах
h1 = 0.10  # 100 метров
h2 = 0.10  # 100 метров
l = 1  # 1000 метров

v1 = 40  # 40 км/ч
v2 = 30  # 30 км/ч

c1 = 0.115  # л/км
c2 = 0.15  # л/км

t_max = 0.028  # максимальное время в часах

# описываем функцию для расчета времени движения
def time_func(x):
    # Движение по полю: вверх до точки x, затем по диагонали
    # Расстояние по первому участку: вертикальное расстояние h1 + горизонтальное x
    dist1 = math.sqrt(h1**2 + x**2)
    # Расстояние по второму участку: от точки (x, h1) до точки (l, h1+h2)
    dist2 = math.sqrt((l - x)**2 + h2**2)
    
    # Время = расстояние / скорость
    time = dist1 / v1 + dist2 / v2
    return time

# описываем функцию для расчета расхода топлива (целевая функция)
def fuel_func(x):
    # Расстояние по первому участку
    dist1 = math.sqrt(h1**2 + x**2)
    # Расстояние по второму участку
    dist2 = math.sqrt((l - x)**2 + h2**2)
    
    # Расход топлива = расстояние * расход на км
    fuel = dist1 * c1 + dist2 * c2
    return fuel

# Создаем функцию для ограничения: время должно быть <= t_max
def constraint_time(x):
    return t_max - time_func(x)  # должно быть >= 0

# Зададим начальное значение x
x0 = 0

# Определяем ограничение
constraint = {'type': 'ineq', 'fun': constraint_time}

# Определяем границы для x (0 <= x <= l)
bounds = [(0, l)]

# Минимизируем расход топлива с учетом ограничения по времени
result = minimize(fuel_func, x0, method='SLSQP', bounds=bounds, constraints=constraint)

print("Результат оптимизации:")
print(result)
print(f"\nОптимальное значение x: {result.x[0]:.4f} км")
print(f"Минимальный расход топлива: {result.fun:.4f} л")
print(f"Время движения: {time_func(result.x[0]):.4f} ч")
print(f"Ограничение по времени выполнено: {time_func(result.x[0]) <= t_max}")